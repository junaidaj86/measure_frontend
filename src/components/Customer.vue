<template>
    <div class="col-md-12">
        <div class="card card-container">
            <img id="profile-img" src="//ssl.gstatic.com/accounts/ui/avatar_2x.png" class="profile-img-card" />
            <Form @submit="handleRegister" :validation-schema="schema">
                <div v-if="!successful">
                    <div class="form-group">
                        <label for="username">Name</label>
                        <Field name="name" type="text" class="form-control" />
                        <ErrorMessage name="name" class="error-feedback" />
                    </div>
                    <div class="form-group">
                        <label for="email">Email</label>
                        <Field name="email" type="email" class="form-control" />
                        <ErrorMessage name="email" class="error-feedback" />
                    </div>
                    <div class="form-group">
                        <label for="phone">Phone Number</label>
                        <Field name="phone" type="phone" class="form-control" />
                        <ErrorMessage name="phone" class="error-feedback" />
                    </div>
                    <div class="form-group">
                        <label for="address">Address</label>
                        <Field name="address" type="address" class="form-control" />
                        <ErrorMessage name="address" class="error-feedback" />
                    </div>

                    <div class="form-group">
                        <button class="btn btn-block" :disabled="loading">
                            <span v-show="loading" class="spinner-border spinner-border-sm"></span>
                            Create
                        </button>
                    </div>
                </div>
            </Form>

            <div v-if="message" class="alert" :class="successful ? 'alert-success' : 'alert-danger'">
                {{ message }}
            </div>
        </div>
    </div>
</template>
  
<script>
import { Form, Field, ErrorMessage } from "vee-validate";
import * as yup from "yup";
import UserService from "../services/customer.service";


export default {
    name: "Customer",
    components: {
        Form,
        Field,
        ErrorMessage,
    }, data() {
        const schema = yup.object().shape({
            username: yup
                .string()
                .required("Username is required!")
                .min(3, "Must be at least 3 characters!")
                .max(20, "Must be maximum 20 characters!"),
            email: yup
                .string()
                .required("Email is required!")
                .email("Email is invalid!")
                .max(50, "Must be maximum 50 characters!"),
            password: yup
                .string()
                .required("Password is required!")
                .min(6, "Must be at least 6 characters!")
                .max(40, "Must be maximum 40 characters!"),
        });

        return {
            successful: false,
            loading: false,
            message: "",
            schema,
        };
    },
   
    mounted() {
        if (this.loggedIn) {
            this.$router.push("/profile");
        }
    },

    methods: {
        handleRegister(customer) {
            console.log(JSON.stringify(customer));
            this.message = "";
            this.successful = false;
            this.loading = true;

            UserService.createCustomer(customer).then(
                (response) => {
                    console.log("ggggggg" + JSON.stringify(response));
                    this.message = response.message;
                    this.loading = false;
                    this.$router.push("/customer");
                },
                (error) => {
                    this.content =
                        (error.response &&
                            error.response.data &&
                            error.response.data.message) ||
                        error.message ||
                        error.toString();
                }
            );
        },
    },
};
</script>
  
  